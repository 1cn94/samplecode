package entity;

public class Begeni {
    private int id;
    private String puan;

    public Begeni() {
    }

    public Begeni(int id, String puan) {
        this.id = id;
        this.puan = puan;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPuan() {
        return puan;
    }

    public void setPuan(String puan) {
        this.puan = puan;
    }
    
    
}
